class Unary{

	public static void main(String [] args){

		int x = 5;
		int y = 7;

		System.out.println(++x);
		System.out.println(++y);
		System.out.println(--x);
		System.out.println(--y);
		System.out.println(x);
		System.out.println(y);
	}
}

/* ++x is called as Pre-increment addition first and also print value on same line
 * x++ is called as posr-incriment print first and increment is added to next line  */


