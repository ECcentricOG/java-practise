/* Write a java program, Take a no and print whether it is less than 10 or greater than 10  */

class Demo{

	public static void main(String [] args){

		int var = 35;

		if(var < 10){
			System.out.println(var+" is less than 10");
		}else if(var == 10){
			System.out.println(var+" is equal to 10");
		}else{
			System.out.println(var+" is greater than 10");
		}
	}
}
